# List and Tuples are sequence types. That means we can use indexing to access individual items in a list/tuple.
# Dictionaries and sets are not sequences therefore we cannot access the items using index positions.
# When dealing with items in a dictionary, we use a key to get individual values from the dictionary.
# Dictionaries store key/value pairs.
# A dictionary is a collection of values, that are stored using a key.
# A set is an unordered collection of things. There is no way to retrieve a specific item from a set.
# A key in a dictionary should be unique on each dictionary item. We can't use the same key on more than one value.
